    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        if (!txtUsuario.getText().isEmpty() &&
                !txtApellido.getText().isEmpty() &&
                !txtNombre.getText().isEmpty() &&
                !txtCorreo.getText().isEmpty() &&
                !txtContrasenia.getText().isEmpty() &&
                txtRepetirContrasenia.getText().equals(txtContrasenia.getText())) {
            Usuario usur = new Usuario(txtUsuario.getText(),txtContrasenia.getText(),txtNombre.getText(),txtApellido.getText(),txtCorreo.getText() );
            if (UsuarioLOGIC.insertar(usur)) {
                JOptionPane.showMessageDialog(this, "USUARIO REGISTRADO CORRECTAMENTE  ");
            }else{
                JOptionPane.showMessageDialog(this, "USUARIO YA EXISTENTE");
            }
        }else{
             JOptionPane.showMessageDialog(this, "COMPLETE LOS CAMPOS");
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
           if (!txtUsuario.getText().isEmpty() &&
                !txtApellido.getText().isEmpty() &&
                !txtNombre.getText().isEmpty() &&
                !txtCorreo.getText().isEmpty() &&
                !txtContrasenia.getText().isEmpty() &&
                txtRepetirContrasenia.getText().equals(txtContrasenia.getText())) {
            Usuario usur = new Usuario(txtUsuario.getText(),txtContrasenia.getText(),txtNombre.getText(),txtApellido.getText(),txtCorreo.getText() );
            if (UsuarioLOGIC.modificar(usur)) {
                JOptionPane.showMessageDialog(this, "USUARIO MODIFICADO CORRECTAMENTE  ");
            }else{
                JOptionPane.showMessageDialog(this, "USUARIO NO ENCONTRADO");
            }
        }else{
             JOptionPane.showMessageDialog(this, "COMPLETE LOS CAMPOS");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
       if (!txtUsuario.getText().isEmpty()) {
             if (UsuarioLOGIC.eliminar(txtUsuario.getText())) {
                JOptionPane.showMessageDialog(this, "USUARIO ELIMINADO CORRECTAMENTE  ");
            }else{
                JOptionPane.showMessageDialog(this, "USUARIO NO ENCONTRADO");
            }
        }else{
             JOptionPane.showMessageDialog(this, "COMPLETE LOS CAMPOS");
    }//GEN-LAST:event_btnEliminarActionPerformed
    }
